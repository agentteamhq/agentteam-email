import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const docsRoot = resolve(scriptDir, '..')

const snippets = [
  {
    source: 'examples/self-host/email-routing.json',
    output: 'snippets/generated/examples/self-host/email-routing-json.mdx',
    language: 'json',
    title: 'examples/self-host/email-routing.json',
    options: 'lines'
  }
]

for (const snippet of snippets) {
  const sourcePath = resolve(docsRoot, snippet.source)
  const outputPath = resolve(docsRoot, snippet.output)
  const source = await readFile(sourcePath, 'utf8')
  const content = [
    `{/* Generated by docs/scripts/generate-example-snippets.mjs from ${snippet.source}. */}`,
    '',
    `\`\`\`${snippet.language} title="${snippet.title}" ${snippet.options}`,
    source.trimEnd(),
    '```',
    ''
  ].join('\n')

  await mkdir(dirname(outputPath), { recursive: true })
  await writeFile(outputPath, content, 'utf8')
  console.log(`Wrote ${outputPath}`)
}
