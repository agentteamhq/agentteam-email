import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const docsRoot = resolve(scriptDir, '..')
const repoRoot = resolve(docsRoot, '..')

const snippets = [
  {
    source: 'docs/examples/self-host/email-routing.json',
    output: 'docs/snippets/generated/examples/self-host/email-routing-json.mdx',
    language: 'json',
    title: 'docs/examples/self-host/email-routing.json',
    options: 'lines'
  },
  {
    source: 'skills/at-email-cli/SKILL.md',
    output: 'docs/snippets/generated/skills/at-email-cli-skill.mdx',
    language: 'md',
    title: 'skills/at-email-cli/SKILL.md',
    options: 'lines',
    fence: '````'
  }
]

for (const snippet of snippets) {
  const sourcePath = resolve(repoRoot, snippet.source)
  const outputPath = resolve(repoRoot, snippet.output)
  const source = await readFile(sourcePath, 'utf8')
  const fence = snippet.fence ?? '```'
  const content = [
    `{/* Generated by docs/scripts/generate-example-snippets.mjs from ${snippet.source}. */}`,
    '',
    `${fence}${snippet.language} title="${snippet.title}" ${snippet.options}`,
    source.trimEnd(),
    fence,
    ''
  ].join('\n')

  await mkdir(dirname(outputPath), { recursive: true })
  await writeFile(outputPath, content, 'utf8')
  console.log(`Wrote ${outputPath}`)
}
