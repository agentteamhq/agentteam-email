import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, relative, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

import { toOpenAPISchema } from '@elysiajs/openapi'
import { createJiti } from 'jiti'
import { stringify } from 'yaml'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(scriptDir, '..')
const outputFile = resolve(repoRoot, 'packages/backend/openapi/rpc.openapi.yaml')
const checkOnly = process.argv.includes('--check')

process.env.NODE_ENV = 'test'
process.env.PUBLIC_HOSTNAME = 'https://mail.example.com'
process.env.DATABASE_URL = 'mongodb://127.0.0.1:27017/agentteam-email-openapi'
process.env.E2E_TEST_SUPPORT_ENABLED = 'false'
process.env.ENCRYPT_SECRET_KEY = 'AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA'

const jiti = createJiti(import.meta.url, {
  interopDefault: true
})

const rpcModule = await jiti.import('../packages/backend/src/rpc/index.ts')
const backendRpcApp = rpcModule.backendRpcApp ?? rpcModule.default?.backendRpcApp

if (!backendRpcApp) {
  throw new Error('Unable to load backendRpcApp from packages/backend/src/rpc/index.ts')
}

const rpcSchema = toOpenAPISchema(backendRpcApp)

for (const path of Object.keys(rpcSchema.paths)) {
  if (
    path === '/rpc/agent-mail/ingest/v1' ||
    path === '/rpc/auth' ||
    path.startsWith('/rpc/auth/')
  ) {
    delete rpcSchema.paths[path]
  }
}

const openapiDocument = {
  openapi: '3.0.3',
  info: {
    title: 'Agentteam Email RPC',
    version: '2026-06-19',
    description:
      'Static OpenAPI contract for first-party same-origin /rpc routes. This file is committed for internal service-client validation and is not served as public API documentation.'
  },
  servers: [
    {
      url: process.env.PUBLIC_HOSTNAME,
      description: 'Configured app origin'
    }
  ],
  tags: [
    {
      name: 'System',
      description: 'Health and authenticated session inspection routes.'
    },
    {
      name: 'Cloudflare',
      description: 'Cloudflare OAuth connection and domain provisioning routes.'
    }
  ],
  paths: rpcSchema.paths,
  components: rpcSchema.components
}

const generated = `# This file is generated by scripts/generate-rpc-openapi.mjs.\n# Run: mise run typegen\n${stringify(
  openapiDocument,
  {
    aliasDuplicateObjects: false,
    lineWidth: 0
  }
)}`
const relativeOutputFile = relative(repoRoot, outputFile)

if (checkOnly) {
  const existing = await readFile(outputFile, 'utf8').catch(() => '')
  if (existing !== generated) {
    throw new Error(`${relativeOutputFile} is stale. Regenerate the checked-in RPC OpenAPI schema.`)
  }
  console.log(`${relativeOutputFile} is up to date`)
} else {
  await mkdir(dirname(outputFile), { recursive: true })
  await writeFile(outputFile, generated, 'utf8')
  console.log(`Wrote ${relativeOutputFile}`)
}
